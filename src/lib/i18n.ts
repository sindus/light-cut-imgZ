export function getT(lang: Lang) {
  return (key: string, vars?: Record<string, string>): string => {
    let s = dict[lang][key] ?? dict['en'][key] ?? key
    if (vars) for (const [k, v] of Object.entries(vars)) s = s.replace(`{${k}}`, v)
    return s
  }
}
